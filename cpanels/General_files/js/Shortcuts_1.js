
$(document).keypress(function (e) {
    var code = e.keyCode;
 
    if (code == 49) {
        var url = "index_pag.php";
        window.open(url, '_blank');
    } else if (code == 50) {
        var url = "index_pag_eng.php";
        window.open(url, '_blank');

    } else if (code == 51) {
        var url = "Employee.php";
        window.open(url, '_blank');

    } else if (code == 52) {
        var url = "Employee_eng.php";
        window.open(url, '_blank');

    } else if (code == 53) {
        var url = "Visit.php";
        window.open(url, '_blank');

    } else if (code == 54) {
        var url = "Visit_eng.php";
        window.open(url, '_blank');

    } else if (code == 55) {
        var url = "footer_pag.php";
        window.open(url, '_blank');

    }  else if (code == 56) {
        var url = "footer_pag.php";
        window.open(url, '_blank');

    }  else if (code == 57) {
        var url = "Registration.php";
        window.open(url, '_blank');

    }  else if (code == 48) {
        var url = "Registration_eng.php";
        window.open(url, '_blank');

    } 






});



